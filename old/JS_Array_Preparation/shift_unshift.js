// unshift() method adds new elements to the beginning of an array
//
fruits = ["Banana", "Orange", "Apple", "Mango"];
fruits.unshift("Lemon", "Pineapple");
console.log(fruits)
    //
    // Shift (remove) the first element of the array
fruits = ["Banana", "Orange", "Apple", "Mango"];
fruits.shift();
console.log(fruits)